package com.practice.daily;

public class Product {
	int id;
	String productName;
	String dealer;
	int price;
	String customerName;
	int age;
	String state;
	String company;
	int salary;
	String gender;
	public Product(int id, String productName, String dealer, int price, String customerName, int age, String state,
			String company, int salary, String gender) {
		super();
		this.id = id;
		this.productName = productName;
		this.dealer = dealer;
		this.price = price;
		this.customerName = customerName;
		this.age = age;
		this.state = state;
		this.company = company;
		this.salary = salary;
		this.gender = gender;
	}
	
	

}
