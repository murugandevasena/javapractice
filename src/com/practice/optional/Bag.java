package com.practice.optional;

public class Bag {
	private String brand;
	private int noOfSize ;
	private boolean isWaterProff;
	private String colour;
	public String getBrand() {
		return brand;
	}
	public void setBrand(String brand) {
		this.brand = brand;
	}
	public int getNoOfSize() {
		return noOfSize;
	}
	public void setNoOfSize(int noOfSize) {
		this.noOfSize = noOfSize;
	}
	public boolean isWaterProff() {
		return isWaterProff;
	}
	public void setWaterProff(boolean isWaterProff) {
		this.isWaterProff = isWaterProff;
	}
	public String getColour() {
		return colour;
	}
	public void setColour(String colour) {
		this.colour = colour;
	}
	
	
	

}
